interface User {
    /**
     * The name of the user.
     *
     * @var {string}
     */
    name: string;

    /**
     * The email address of the user.
     *
     * @var {string}
     */
    email: string;

    /**
     * The password of the user.
     *
     * @var {string}
     */
    password: string;
}

export default User;
